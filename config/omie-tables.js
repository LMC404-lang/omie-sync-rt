'use strict';

/**
 * ============================================================
 *  Omie Table Registry — declarative configuration
 * ============================================================
 *
 *  Single source of truth for every Omie endpoint the pipeline
 *  syncs to Supabase. NO raw / NO jsonb columns: every Omie
 *  field maps to its own typed Postgres column. Array fields
 *  (line items, characteristics, etc.) are extracted into
 *  dedicated child tables.
 *
 *  Per-entry fields
 *  ----------------
 *
 *    name                Internal entity slug (CLI / sync_log key).
 *    table               Supabase parent table name.
 *    endpoint            Omie API path (e.g. "geral/clientes").
 *    action              Omie API call name (e.g. "ListarClientes").
 *    listKey             Response property that holds the records.
 *                        Generic extractor auto-detects when wrong.
 *    idFields            Ordered list of source fields to use as PK.
 *                        First non-null wins. SHA-1 hash fallback.
 *    pkType              'text' (default) | 'bigint'.
 *    params              Extra params merged into every request.
 *
 *  Pagination control
 *  ------------------
 *
 *    paginationStyle     'standard' (default — pagina/registros_por_pagina/total_de_paginas)
 *                        'new'      (nPagina/nRegPorPagina/nTotPaginas)
 *                        'nfe'      (nPagina/nRegistrosPorPagina/nTotalPaginas)
 *                        'pedidocompra' (nPagina/nRegsPorPagina/nTotalPaginas)
 *    paginated           false → single call, no pagination params.
 *    pageParam,
 *    sizeParam,
 *    totalPagesField     Manual overrides (rarely needed).
 *
 *  Date filters
 *  ------------
 *
 *    sinceDateField      Incremental sync sends this filter
 *                        (DD/MM/YYYY) when a since-timestamp exists.
 *    sinceEndDateField   Companion start/end filter.
 *    defaultSinceDays    Fallback lookback in days when `since`
 *                        is null and the endpoint requires dates.
 *
 *  Columns (one Postgres column per Omie field)
 *  --------------------------------------------
 *
 *    columns             Array of {name, from, type?} mapping a JSON
 *                        path in the Omie record to a typed Postgres
 *                        column. Type-converted by the transformer.
 *
 *      name              Postgres column name (snake_case).
 *      from              JSON path. Dot notation for nested:
 *                        'cabecalho.nNF' → record.cabecalho.nNF.
 *      type              'text' (default) | 'numeric' | 'bigint' |
 *                        'date' | 'timestamptz' | 'boolean' |
 *                        'boolean_inverted'.
 *                          - boolean: Omie 'S' → true, 'N' → false
 *                          - boolean_inverted: 'S' → false, 'N' → true
 *                            (used to flip inativo → ativo, etc.)
 *
 *  Children (arrays-of-objects pivoted into child tables)
 *  ------------------------------------------------------
 *
 *    children            Array of nested-array definitions. Each emits
 *                        a separate Postgres table whose rows are the
 *                        line-items of the array, with a foreign-key
 *                        column pointing back at the parent omie_id.
 *
 *      table             Child Postgres table name.
 *      listFrom          Path to the array on the parent record
 *                        (dot notation; e.g. 'det' or 'pedido.det').
 *      parentRefColumn   Column on the child table holding parent PK.
 *      idFields          Fields on each child record to use as its PK.
 *                        First non-null wins; fallback is a stable
 *                        hash of (parent_id, row_index, content).
 *      pkType            'text' (default) | 'bigint'.
 *      columns           Same shape as parent columns.
 *      childChildren     Recursion: a child may itself have children.
 *
 *  Lifecycle
 *  ---------
 *
 *    tier                'critical' | 'important' | 'optional' | 'industry'
 *    phase               1..7 (lookups → industry-specific)
 *    purpose             Human description.
 *    enabled             true|false. Forces opt-in/out regardless of tier.
 *    note                Free-text note shown beside disabled entries.
 *    legacy              true for hand-tuned core entities whose
 *                        omie_id is bigint. Used by the schema
 *                        generator to apply FK constraints and to
 *                        bypass the registry-driven extractor in
 *                        favour of the bespoke one when needed.
 * ============================================================
 */

