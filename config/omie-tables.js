/**
 * Decide whether an entry should be active for the current run, based
 * on per-entry `enabled` overrides and the OMIE_SYNC_TIERS env var.
 *
 * Legacy entries (phase 0) are always-on regardless of tier filter.
 */
function isEnabled(entry, enabledTiers) {
  if (typeof entry.enabled === 'boolean') return entry.enabled;
  if (entry.legacy) return true;
  if (enabledTiers.includes('all')) return true;
  return enabledTiers.includes(entry.tier);
}

function getEnabledTiers() {
  return (process.env.OMIE_SYNC_TIERS || 'critical,important,optional')
    .toLowerCase()
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean);
}

function activeTables() {
  const enabledTiers = getEnabledTiers();
  return TABLES.filter((entry) => isEnabled(entry, enabledTiers));
}

